package com.example;

public class Main {
    public static void main(String[] args) {
        // Tipo de datos numericos
        byte number1 = 1; //1 byte
        short number2 = 2; //2 byte
        int number3 = 3; //4 byte
        long number4 = 4L; //8 byte

        float decimal1 = 4.9f;
        double decimal2 = 9.99d;

        // Tipo de datos String y caracteres
        String nombre = "Ibrahim";
        char caracter1 = 'a';

        //Tipo de dato booleanos
        boolean verdadero = true;
        boolean falso = false;

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);

        System.out.println(decimal1);
        System.out.println(decimal2);

        System.out.println(nombre);
        System.out.println(caracter1);

        System.out.println(verdadero);
        System.out.println(falso);
    }
}